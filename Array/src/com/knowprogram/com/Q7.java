package com.knowprogram.com;

public class Q7 {
	
	public static void main(String[] args) {
		
		//Sum of Two Arrays Elements
		
		int[] arr1 = {1,2,3,4,5};
		int[] arr2 = {1,2,3,4,5};
		
		System.out.print('[');
		for(int i=0; i<arr1.length; i++) {
			System.out.print(arr1[i]+arr2[i]+" ");
		}
		System.out.print(']');
		
	}
}
